# Python Data Structures

# Lists
# Lists are ordered, mutable, and allows duplicate elements
my_list = [1, 2, 3, 4, 5]
print("List: ", my_list)

# Tuples
# Tuples are ordered, immutable, and allows duplicate elements
my_tuple = (1, 2, 3, 4, 5)
print("Tuple: ", my_tuple)

# Sets
# Sets are unordered, mutable, and does not allow duplicate elements
my_set = {1, 2, 3, 4, 5}
print("Set: ", my_set)

# Dictionaries
# Dictionaries are unordered, mutable, and indexed. No duplicate members.
my_dict = {
    "name": "John",
    "age": 30,
    "city": "New York"
}
print("Dictionary: ", my_dict)